# Задача 6: Вы пользуетесь общественным транспортом? Вероятно, вы расплачивались за проезд и получали билет 
# с номером. Счастливым билетом называют такой билет с шестизначным номером, где сумма первых трех цифр равна 
# сумме последних трех. Т.е. билет с номером 385916 – счастливый, т.к. 3+8+5=9+1+6. 
# Вам требуется написать программу, которая проверяет счастливость билета.

# 385916 -> yes
# 123456 -> no

num = input('Введите номер билета: ')
if int(len(num))==6:
    sum1 = int(num[0]) + int(num[1]) + int(num[2])
    sum2 = int(num[3]) + int(num[4]) + int(num[5])
    if sum1 == sum2:
        print('Счастливый билет!!!')
    else:
        print('Обычный(')
else:
    print ('Вы ввели неправильный номер!')

# Задача 8: Требуется определить, можно ли от шоколадки размером n × m долек отломить k долек, 
# если разрешается сделать один разлом по прямой между дольками (то есть разломить шоколадку на два прямоугольника).

# 3 2 4 -> yes
# 3 2 1 -> no

n = int(input('Введите ширину плитки шоколада: '))
m = int(input('Введите длину плитки шоколада: '))
k = int(input('Введите количество долек, которые нужно отделить: '))
if k <= m*n and (k % n == 0 or k % m == 0) :
    print ('Плитку разделить можно')
else:
    print ('Плитку разделить нельзя')
