# Задача 2: Найдите сумму цифр трехзначного числа.

# 123 -> 6 (1 + 2 + 3)
# 100 -> 1 (1 + 0 + 0)

n =int ( input ('Введите трехзначное число: '))
if 99 < n and n < 1000:
    print (f'Сумма цифр числа {n}: ',n//100 + n//10%10 + n%10)
else: 
    print ('Вы ввели не трехзначное число!' )

